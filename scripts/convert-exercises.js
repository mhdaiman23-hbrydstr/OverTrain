const XLSX = require('xlsx');
const fs = require('fs');
const path = require('path');
const crypto = require('crypto');

// Read the Excel file
const workbook = XLSX.readFile(path.join(__dirname, '..', 'exercise_library_full.xlsx'));
const sheetName = workbook.SheetNames[0];
const worksheet = workbook.Sheets[sheetName];

// Convert to JSON
const data = XLSX.utils.sheet_to_json(worksheet);

console.log(`Found ${data.length} exercises in Excel file`);
console.log('Sample row:', data[0]);

// Transform the data
const exercises = data.map((row, index) => {
  const name = row['Name'] || row['name'] || '';
  const bodyPart = row['Body Part'] || row['body part'] || row['BodyPart'] || '';
  const equipment = row['Equipment Type'] || row['equipment type'] || row['EquipmentType'] || '';

  // Generate a simple numeric ID
  const id = (index + 1).toString();

  // Create category string: "BODYPART - EQUIPMENT"
  const category = `${bodyPart.toUpperCase()} - ${equipment.toUpperCase()}`;

  // Muscle group is just the body part
  const muscleGroup = bodyPart;

  return {
    id,
    name,
    category,
    muscleGroup
  };
});

// Generate TypeScript file content
const tsContent = `// Auto-generated from exercise_library_full.xlsx
// Do not edit this file manually

export interface Exercise {
  id: string
  name: string
  category: string
  muscleGroup: string
}

export const EXERCISES: Exercise[] = ${JSON.stringify(exercises, null, 2)}
`;

// Write to file
const outputPath = path.join(__dirname, '..', 'lib', 'exercise-data.ts');
fs.writeFileSync(outputPath, tsContent, 'utf-8');

console.log(`✅ Successfully converted ${exercises.length} exercises`);
console.log(`📝 Output written to: lib/exercise-data.ts`);

// Log summary by body part
const byBodyPart = exercises.reduce((acc, ex) => {
  acc[ex.muscleGroup] = (acc[ex.muscleGroup] || 0) + 1;
  return acc;
}, {});

console.log('\n📊 Exercises by body part:');
Object.entries(byBodyPart)
  .sort((a, b) => a[0].localeCompare(b[0]))
  .forEach(([part, count]) => {
    console.log(`  ${part}: ${count}`);
  });
